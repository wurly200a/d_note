#ifndef IOWND_H

/********************************************************************************
 * 内容  : IOウィンドウクラスの登録、ウィンドウの生成
 * 引数  : HWND hWnd
 * 引数  : LOGFONT *logFontPtr
 * 戻り値: HWND
 ***************************************/
HWND IoWndCreate( HWND hWnd, LOGFONT *logFontPtr );

/********************************************************************************
 * 内容  : IOウィンドウのフォント変更
 * 引数  : LOGFONT *logFontPtr
 * 戻り値: なし
 ***************************************/
void IoWndChangeFont( LOGFONT *logFontPtr );

/********************************************************************************
 * 内容  : IOウィンドウのサイズ変更
 * 引数  : int x
 * 引数  : int y
 * 引数  : int cxClient
 * 引数  : int cyClient
 * 戻り値: BOOL
 ***************************************/
BOOL IoWndSize( int x, int y, int cxClient, int cyClient );

/********************************************************************************
 * 内容  : IOウィンドウの破壊
 * 引数  : int nCmdShow
 * 戻り値: BOOL
 ***************************************/
void IoWndDestroy( void );

/********************************************************************************
 * 内容  : 文字列出力
 * 引数  : TCHAR* strPtr
 * 引数  : int length
 * 戻り値: なし
 ***************************************/
void IoWndPrint( TCHAR* strPtr, int length );

#define IOWND_H
#endif /* IOWND_H */
